    protected void datatable(){
     Object[] Baris = {"ID Petugas", "Nama Petugas", "NIP", "No.Kontak"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtpetugas.getText(); // Sesuaikan nama komponen textfield cari Anda
        try {
            String sql = "SELECT * FROM tugas WHERE idptgs LIKE '%"+cariitem+"%' OR nmptgs LIKE '%"+cariitem+"%'";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString("idptgs"), // Sesuaikan nama kolom DB Anda
                    hasil.getString("nmptgs"),
                    hasil.getString("nip"),
                    hasil.getString("notlp"),
                    
                });
            }
            popuppetugas.setModel(tabmode); // Sesuaikan nama JTable Anda
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil: " + e);
        }
    }

