 protected void datatable(){
        Object[] Baris = {"Id_Tanah", "Lokasi", "Status_Tanah"
                , "No_Berkas","Luas"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcari.getText();
         try {
            String sql = "SELECT * FROM tanah where Id_Tanah like '%" + cariitem +
                    "%' or Id_Tanah like '%" + cariitem + "%' order by Id_Tanah asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5)
                });
            }
            tbltanah.setModel(tabmode);
         }catch (Exception e){
             JOptionPane.showMessageDialog(null,"data gagal di panggil" +e);
         }
    }
